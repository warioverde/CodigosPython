letra = input("Ingrese vocal: ")

if(letra.lower() == "a"):
    print("Su letra es a")
elif(letra.lower() == "e"):
    print("su letra es e")
elif(letra.lower() == "i"):
    print("su letra es i")
elif(letra.lower() == "o"):
    print("Su letra es o")
elif(letra.lower()== "u"):
    print("Su letra es u")
else:
    print("Su letra no es una vocal")